#  N-Queen: クイーンの効き筋
#  =========================
#  クイーンの位置から、縦、横、斜めが効き筋となります。
#
#  　　       column(列)
#  row(行)_0___1___2___3___4_
#       0|-*-|---|---|-*-|---|
#        +-------------------+
#       1|---|-*-|---|-*-|---|
#        +-------------------+ 
#       2|---|---|-*-|-*-|-*-| 
#        +-------------------+ 
#       3|-*-|-*-|-*-|-Q-|-*-|
#        +-------------------+
#       4|---|---|-*-|-*-|-*-|
#        +-------------------+
#
#
#  N-Queen: 盤面上で互いのクイーンが効き筋にならないように配置
#  ===========================================================
#
#        完成図は以下の通りです。
#
#  　　       column(列)
#  row(行)_0___1___2___3___4_
#       0|-Q-|---|---|---|---|
#        +-------------------+
#       1|---|---|---|-Q-|---|
#        +-------------------+ 
#       2|---|-Q-|---|---|---| 
#        +-------------------+ 
#       3|---|---|---|---|-Q-|
#        +-------------------+
#       4|---|---|-Q-|---|---|
#        +-------------------+
#
#
#  効き筋の表現
#  ============
#
#  クイーンの位置から下側を走査対象とします。
#
#  　すでに効き筋：FALSE(盤面ではF）
#  　配置可能    ：TRUE
#
#  　　       column(列) 
#  row(行)_0___1___2___3___4_
#       0|---|---|---|---|---| 
#        +-------------------+
#       1|---|---|---|---|---|
#        +-------------------+ 
#       2|---|-Q-|---|---|---| 
#        +-------------------+ 
#       3|-F-|-F-|-F-|---|---|
#        +-------------------+
#       4|---|-F-|---|-F-|---|
#        +-------------------+
#                      
#
#  効き筋を三つの配列で表現
#  ========================
#
#  ■ 基本：aBoard[row]=col
#           aBoard[2  ]=1
#
#  　　       column(列)
#  row(行)_0___1___2___3___4_
#       0|---|---|---|---|---|
#        +-------------------+
#       1|---|---|---|---|---|
#        +-------------------+ 
#       2|---|-Q-|---|---|---| aBoard[2]=1 に配置
#        +-------------------+
#       3|---|---|---|---|---|
#        +-------------------+
#       4|---|---|---|---|---|
#        +-------------------+
#
#
#  ■配列1：down[row]
#
#  そのrow(行)にQueenがいる場合はFALSE
#                      いない場合はTRUE
#
#  　　       column(列)
#  row(行)_0___1___2___3___4_
#       0|---|---|---|---|---|
#        +-------------------+
#       1|---|---|---|---|---|
#        +-------------------+ 
#       2|---|-Q-|---|---|---| 
#        +-------------------+
#       3|---|-F-|---|---|---|
#        +-------------------+
#       4|---|-F-|---|---|---|
#        +-------------------+
#             down[col(1)]==false (すでに効き筋）
#
#
#  ■配列２：right[col-row+N-1]
#                    right[col-row+N-1]==F
#                        Qの場所：col(1)-row(2)+(4-1)=2なので
#                        col-row+N-1が２のところがＦとなる 
#  　　       column(列)
#  row(行)_0___1___2___3___4_
#       0|---|---|---|---|---|
#        +-------------------+
#       1|---|---|---|---|---|
#        +-------------------+ 
#       2|---|-Q-|---|---|---| 
#        +-------------------+ 
#       3|---|---|-F-|---|---|
#        +-------------------+
#       4|---|---|---|-F-|---|
#        +-------------------+
#                      right[col-row+(N-1)]==false(すでに効き筋）
#
#
#  ■配列3：left[col+row]
#                      left[col+row]==F 
#                          Qの場所：col(1)+row(2)=3なので
#                          col+rowが3になるところがFとなる。
#
#  　　       column(列) 
#  row(行)_0___1___2___3___4_
#       0|---|---|---|---|---|
#        +-------------------+
#       1|---|---|---|---|---|
#        +-------------------+ 
#       2|---|-Q-|---|---|---| 
#        +-------------------+ 
#       3|-F-|---|---|---|---|
#        +-------------------+
#       4|---|---|---|---|---|
#        +-------------------+
#      left[col+row]
#
#
#  ステップ１
#  ==========
#  row=0, col=0 にクイーンを配置してみます。
#
#  aBoard[row]=col
#     ↓
#  aBoard[0]=0;
#
#  　　       column(列) 
#  row(行)_0___1___2___3___4_
#   ->  0|-Q-|---|---|---|---| aBoard[row]=col
